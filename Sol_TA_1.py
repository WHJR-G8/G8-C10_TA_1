l1 = [1,2,3,4,5,7,9]
print("The original list is : \n", l1)

l1.insert(5,6)
print("\nThe list after inserting 6 at position 5 is : \n", l1)

l1.remove(9)
print("\nThe list after removing element 9 is : \n", l1)

l1.append(8)
print("\nThe list after appending element 8 is is : \n", l1)

l1.pop()
print("\nThe list after popping out the last element is : \n", l1)